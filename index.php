                <h2>Blood Donors</h2>
                <p class="quote">The most common blood type is O, followed by type A.</p>
                <p class="text">Type O negative blood is considered the universal blood type. People with type O negative blood are called universal donors because type O negative blood is compatible to any blood recipient’s type. Ideally the donor’s blood types should always be an exact match to the recipient’s blood type. Because many instances where a blood transfusion is needed is in an emergency situation, type O negative blood is often in short supply which further increases the need for type O negative blood donors to generously donate their blood to hospitals and blood banks.</p>
                <p class="text">People with type AB blood are considered universal blood recipients. The reason is that AB blood types do not contain natural antibodies against the ABO blood groups and this avoids incompatibility reactions when a person who is blood group AB receives blood from a donor who has another ABO blood group. Blood group AB is very rare and although AB blood types are able to receive any type of blood they are not able to donate blood to persons that are not blood group AB.</p>
